package com.sync.Antic.model;

import jakarta.persistence.*;

/**
 *
 * @author berna
 */
@Entity
@Table(name = "roles")
public class Role {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(unique = true, nullable = false)
    private String name;
}
